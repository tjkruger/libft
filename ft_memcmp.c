#include <stdio.h>

int	ft_memcmp(const void *s1, const void *s2, int n)
{
	const unsigned char	*ptr1;
	const unsigned char	*ptr2;
	int					i;

	ptr1 = s1;
	ptr2 = s2;
	i = 0;
	while (i < n)
	{
		if (ptr1[i] != ptr2[i])
		{
			return (ptr1[i] - ptr2[i]);
		}
		i++;
	}
	return (0);
}
